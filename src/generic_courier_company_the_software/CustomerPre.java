    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty()==true||jPasswordField1.getPassword().length==0){
            CustomerInvalid inv = new CustomerInvalid("Empty fields detected");
            inv.setVisible(true);
        }
        else{
            String user = jTextField1.getText().toString();
            char [] pass = jPasswordField1.getPassword();
            String word = String.copyValueOf(pass);
            if(validate_login(user,word)==true){
                CustomerMain2 main = new CustomerMain2(user);
                main.setVisible(true);
                this.dispose();
            }
            else{
                CustomerInvalid inv = new CustomerInvalid("Account does not exist");
                inv.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private boolean validate_login(String username,String password){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/courierservice?" + "user=root&password=");
            PreparedStatement pst = conn.prepareStatement("Select * from dataz where login=? and password=?");
                pst.setString(1, username);
                pst.setString(2, password);
                ResultSet rs = pst.executeQuery();                        
       if(rs.next())            
           return true;    
       else
           return false;            

        }
        catch(Exception e){
            e.printStackTrace();
            return false;

        }
    }
