    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(jTextField3.getText().isEmpty()==true||jPasswordField2.getPassword().length==0){
            CustomerInvalid inv = new CustomerInvalid("Empty fields detected");
            inv.setVisible(true);
        }
        else{
            if(validate_register(jTextField3.getText().toString())==false){
                try{
                Class.forName("com.mysql.jdbc.Driver");  
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/courierservice?" + "user=root&password="); 
                PreparedStatement pst = conn.prepareStatement("INSERT INTO dataz (login,password) values (?,?)");
                char pass[] = jPasswordField2.getPassword();
                String pas = String.copyValueOf(pass);
                pst.setString(1, jTextField3.getText().toString()); 
                pst.setString(2,pas );
                pst.executeUpdate();
                }
                catch(Exception e){
                    e.printStackTrace();
                }
                String name = jTextField3.getText().toString();
                CustomerYay yay = new CustomerYay(name);
                yay.setVisible(true);
                this.dispose();
            }
            else{
                CustomerInvalid inv = new CustomerInvalid("Username already exsists");
                inv.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

